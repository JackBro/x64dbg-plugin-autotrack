class DllName
{
public:
	const char* pName;
	DllName(const char* p)
	{
		pName = p;
	}

	friend bool operator==(const DllName& left , const DllName& right)
	{
		if(left.pName == NULL || right.pName == NULL)
			return false;

		return _stricmp(left.pName , right.pName) == 0;
	}
	friend bool operator<(const DllName& left , const DllName& right)
	{
		if(left.pName == NULL)
			return true;
		if(right.pName == NULL)
			return false;

		return _stricmp(left.pName , right.pName) < 0;
	}

};

map<DllName , int> g_mapDllName;
void AddSystemDllName()
{
	for(int i = 0 ; i < _countof(g_systemDllName); ++i)
		g_mapDllName.insert(pair<DllName , int>(g_systemDllName[ i ] , i));
}

bool IsValidSystemDll(const char* pszModuleName)
{
	return g_mapDllName.find(pszModuleName) != g_mapDllName.end();
}